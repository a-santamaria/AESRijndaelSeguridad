    private void buttonOk2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonOk2MouseClicked
        if(Arrays.equals(passwordContrasenia.getPassword(), passwordRepetir.getPassword()))
        {
            Sistema frame= new Sistema();
            frame.setDefaultCloseOperation(EXIT_ON_CLOSE);
            frame.setVisible(true);
            this.setVisible(false);
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Las contrase単as no coinciden", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonOk2MouseClicked

    private void buttonOk1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonOk1MouseClicked
        if (passwordContrasenia.getPassword().length==0)
        {
            JOptionPane.showMessageDialog(null, "Ingrese una contrase単a", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            labelRepetir.setVisible(true);
            passwordRepetir.setVisible(true);
            buttonOk2.setVisible(true);
        }
    }//GEN-LAST:event_buttonOk1MouseClicked

